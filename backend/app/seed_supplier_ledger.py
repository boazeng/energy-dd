def seed_supplier_ledger(db: Session) -> int:
    """מוסיף שורות כרטסת רק אם הטבלה ריקה. מחזיר כמה נוספו."""
    if db.scalar(select(SupplierLedgerRow.id).limit(1)) is not None:
        return 0
    db.add_all(
        SupplierLedgerRow(
            supplier_name=name,
            account_number=acc,
            opening_balance=opening,
            debit=debit,
            credit=credit,
            balance=balance,
        )
        for name, acc, opening, debit, credit, balance in SEED_LEDGER
    )
    db.commit()
    return len(SEED_LEDGER)
